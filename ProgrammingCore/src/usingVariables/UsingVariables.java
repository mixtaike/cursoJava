package usingVariables;

public class UsingVariables {

	public static void main(String[] args) {
		
		//variaveis são como caixas, onde é possivel armazenar valores.
		// há caixa de varios tamanhos e tipos no Java.
		
		// Primeira maneira de declarar uma varivel e atribuir valor
		int myNumber;
		
		myNumber = 88;
		
		System.out.println(myNumber);
		
		
		// Segunda maneira de declarar uma varivel e atribuir valor
		
		int myNumber2 = 88;
		
		System.out.println(myNumber2);
	}

}
